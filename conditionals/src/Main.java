public class Main {
    public static void main(String[] args) {
        int number = 20;
        if (number < 20) {
            System.out.println("Number less than 20.");
        } else if (number > 20) {
            System.out.println("Number greater than 20");
        } else {
            System.out.println("Number equals to 20");
        }
    }
}